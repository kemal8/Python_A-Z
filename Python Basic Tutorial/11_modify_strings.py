# Upper Case
a = "Hello, World!"
print(a.upper())
print("***")



# Lower Case
b = "Hello, World!"
print(b.lower())
print("***")



# Remove Whitespace
c = " Hello, World! "
print(c)
print(c.strip()) # returns "Hello, World!"
print("***")



# Replace String
d = "Hello, World!"
print(d.replace("H", "J"))
print("***")



# Split String
e = "Hello, World!"
print(e.split(",")) # returns ['Hello', ' World!']
print("***")