# Change Item Value
thislistA = ["apple", "banana", "cherry"]
thislistA[1] = "blackcurrant"
print(thislistA)
print("****")

# Change a Range of Item Values
thislistB = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
thislistB[1:3] = ["blackcurrant", "watermelon"]
print(thislistB)
print("****")

# Insert Items
thislistC = ["apple", "banana", "cherry"]
thislistC.insert(2, "watermelon")
thislistC.insert(1, "Melo")
print(thislistC)
print("****")